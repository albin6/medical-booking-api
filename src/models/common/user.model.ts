import mongoose from "mongoose";
import { User } from "./user";

const UserSchema = new mongoose.Schema<User>({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["patient", "doctor", "admin"],
  },
});

export const UserModel = mongoose.model<User>("user", UserSchema);
